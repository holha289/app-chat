import { useEffect, useRef, useState } from "react";
import { getSocket } from "@app/core/socketIo";
import InCallManager from "react-native-incall-manager";
import {
  mediaDevices,
  RTCPeerConnection,
  RTCIceCandidate,
  RTCSessionDescription,
  MediaStream,
} from "react-native-webrtc";
import { requestMediaPermissions } from "@app/core/permissions";

const pcConfig: RTCConfiguration = {
  iceServers: [
    { urls: ["stun:stun.l.google.com:19302"] },
    { urls: ["stun:stun1.l.google.com:19302"] },
    { urls: ["stun:stun2.l.google.com:19302"] },
    {
      urls: "turn:openrelay.metered.ca:443",
      username: "openrelayproject",
      credential: "openrelayproject",
    },
  ],
  iceCandidatePoolSize: 10,
  iceTransportPolicy: 'all',
  bundlePolicy: 'max-bundle',
  rtcpMuxPolicy: 'require',
};

export const useWebRTC = () => {
  const [localStream, setLocalStream] = useState<MediaStream | null>(null);
  const [remoteStream, setRemoteStream] = useState<MediaStream | null>(null);
  const [isScreenSharing, setIsScreenSharing] = useState(false);
  const [connectState, setConnectState] = useState('disconnected');
  const [isAudioEnabled, setIsAudioEnabled] = useState(true);
  const [isVideoEnabled, setIsVideoEnabled] = useState(true);
  const socketIo = getSocket();
  const localStreamRef = useRef<MediaStream | null>(null);
  const pendingOffer = useRef<RTCSessionDescription | null>(null);
  const pendingCandidates = useRef<Map<string, RTCIceCandidate[]>>(new Map());
  const peerIdRef = useRef<Map<string, RTCPeerConnection>>(new Map()); // roomId -> peerId
  const pcRef = useRef<RTCPeerConnection | null>(null);

  const cleanUp = () => {
    if (peerIdRef.current) {
      peerIdRef.current.forEach((peerConnection, roomId) => {
        console.log(`Cleaning up peer connection for room ${roomId}`);
        peerConnection.close();
      });
      peerIdRef.current.clear();
    }

    if (localStreamRef.current) {
      localStreamRef.current.getTracks().forEach(track => {
        track.stop(); // d·ª´ng c·∫£ mic + camera
      });
      localStreamRef.current = null;
    }

    if (remoteStream) {
      remoteStream.getTracks().forEach(track => {
        track.stop(); // ƒë·∫£m b·∫£o d·ª´ng audio c·ªßa ƒë·ªëi ph∆∞∆°ng
      });
    }

    setLocalStream(null);
    setRemoteStream(null);

    InCallManager.stop(); // d·ª´ng audio manager
    InCallManager.setForceSpeakerphoneOn(false); // reset loa
    InCallManager.setMicrophoneMute(true); // mute mic
    console.log("‚úÖ WebRTC cleanup completed");
  };

  useEffect(() => {
    socketIo?.on('connect', () => console.log("‚úÖ Socket.IO connected"));
    socketIo?.on('connect_error', (err) => console.error("‚ùå Socket.IO error:", err));
    InCallManager.setSpeakerphoneOn(true);
    return () => {
      cleanUp();
    };
  }, []);

  // Kh·ªüi t·∫°o local stream
  const initStream = async () => {
    if (localStreamRef.current) {
      console.log("‚úÖ Stream already initialized:", localStreamRef.current.getTracks());
      return localStreamRef.current;
    }
    try {
      const ok = await requestMediaPermissions();
      console.log("‚úÖ Permissions granted:", ok);
      if (!ok) {
        console.error("‚ùå User denied camera/mic permissions");
        return null;
      }

      const stream = await mediaDevices.getUserMedia({
        audio: true,
        video: {
          facingMode: 'user',
          width: { ideal: 1280 },
          height: { ideal: 720 },
          frameRate: { ideal: 30 }
        }
      });
      console.log("‚úÖ Local stream tracks:", stream.getTracks());

      localStreamRef.current = stream;
      setLocalStream(stream);
      if (!isScreenSharing) {
        InCallManager.start({ media: "video", auto: true, ringback: "_DEFAULT_" });
        console.log("‚úÖ InCallManager started");
      } else {
        InCallManager.start({ media: "audio", auto: true, ringback: "_DEFAULT_" });
      }
      return stream;
    } catch (err) {
      console.error("‚ùå Error init media:", err);
      return null;
    }
  };

  const handleCreateOffer = async (roomId: string, peerId: string) => {
    const stream = await initStream();
    if (!stream) {
      console.error("‚ùå Failed to initialize local stream for caller");
      return;
    }
    // T·∫°o peer connection v√† g·ª≠i l·ªùi m·ªùi
    pcRef.current = new RTCPeerConnection(pcConfig);
    peerIdRef.current.set(peerId, pcRef.current);
    // Th√™m c√°c track t·ª´ local stream v√†o peer connection
    stream.getTracks().forEach((track) => {
      console.log("‚úÖ Adding track:", track.kind, track.id);
      pcRef.current?.addTrack(track, stream);
    });

    pcRef.current.onconnectionstatechange = () => {
      switch (pcRef.current?.connectionState) {
        case "connected":
          console.log("‚úÖ Connection established");
          break;
        case "disconnected":
        case "failed":
          console.warn("‚ö†Ô∏è Connection failed/disconnected");
          cleanUp();
          setConnectState('disconnected');
          break;
        case "closed":
          console.log("‚ÑπÔ∏è Connection closed");
          cleanUp();
          setConnectState('disconnected');
          break;
        default:
          break;
      }
    };

    // S·ª± ki·ªán ICE candidate m·ªõi
    pcRef.current.onicecandidate = (event) => {
      if (event.candidate) {
        console.log("‚úÖ New ICE candidate:", event.candidate);
        socketIo?.emit("call:signal", {
          peerId: peerId, // g·ª≠i v·ªÅ m√°y 2
          roomId,
          candidate: event.candidate,
        });
      } else {
        console.log("‚úÖ All ICE candidates sent");
      }
    };

    // S·ª± ki·ªán nh·∫≠n track t·ª´ remote
    pcRef.current.ontrack = (event) => {
      if (event.streams && event.streams[0]) {
        const stream = event.streams[0];
        console.log("‚úÖ Remote stream received, tracks:", stream.getTracks());
        setRemoteStream(stream as any);
      }
    };

    try {
      const offer = await pcRef.current.createOffer({
        offerToReceiveVideo: true,
        offerToReceiveAudio: true,
        voiceActivityDetection: false,
      });
      await pcRef.current.setLocalDescription(offer);
      // G·ª≠i l·ªùi m·ªùi v·ªÅ m√°y 2
      socketIo?.emit("call:signal", {
        peerId: peerId,
        roomId,
        offer,
      });
    } catch (err) {
      console.error("‚ùå Error creating offer:", err);
    }
  };

  const handleAcceptCall = async (roomId: string, peerId: string, callerId: string) => {
    // S·ª• ki·ªán m√°y 2 nh·∫≠n l·ªùi m·ªùi
    const stream = await initStream();
    if (!stream) {
      console.error("‚ùå Failed to initialize local stream for callee");
      return;
    }
    pcRef.current = new RTCPeerConnection(pcConfig);
    peerIdRef.current.set(peerId, pcRef.current);
    // Th√™m c√°c track t·ª´ local stream v√†o peer connection
    stream.getTracks().forEach((track) => {
      console.log("‚úÖ Adding track:", track.kind, track.id);
      pcRef.current?.addTrack(track, stream);
    });

    pcRef.current.onconnectionstatechange = () => {
      switch (pcRef.current?.connectionState) {
        case "connected":
          console.log("‚úÖ Connection established");
          setConnectState('connected');
          break;
        case "disconnected":
        case "failed":
          console.warn("‚ö†Ô∏è Connection failed/disconnected");
          cleanUp();
          setConnectState('disconnected');
          break;
        case "closed":
          console.log("‚ÑπÔ∏è Connection closed");
          cleanUp();
          setConnectState('disconnected');
          break;
        default:
          break;
      }
    };

    pcRef.current.onicecandidate = (event) => {
      if (event.candidate) {
        console.log("‚úÖ New ICE candidate:", event.candidate);
        socketIo?.emit("call:signal", {
          peerId: callerId, // g·ª≠i v·ªÅ m√°y 1
          roomId,
          candidate: event.candidate,
        });
      } else {
        console.log("‚úÖ All ICE candidates sent");
      }
    };

    pcRef.current.ontrack = (event) => {
      if (event.streams && event.streams[0]) {
        const stream = event.streams[0];
        console.log("‚úÖ Remote stream received, tracks:", stream.getTracks());
        setRemoteStream(stream as any);
      }
    };

    if (!pendingOffer.current) {
      console.error("‚ùå No pending offer to accept");
      return;
    }
    // Thi·∫øt l·∫≠p m√¥ t·∫£ t·ª´ xa 
    await pcRef.current.setRemoteDescription(pendingOffer.current);
    pendingOffer.current = null;
    // C√°c ICE candidate ch·ªù
    if (pendingCandidates.current.has(peerId)) {
      const candidates = pendingCandidates.current.get(peerId);
      if (candidates && candidates.length > 0) {
        for (const c of candidates) {
          await pcRef.current.addIceCandidate(new RTCIceCandidate(c));
          console.log("‚úÖ Pending candidate added:", c);
        }
      }
      pendingCandidates.current.delete(peerId);
    }
    // T·∫°o v√† g·ª≠i c√¢u tr·∫£ l·ªùi
    InCallManager.stopRingback();  // d·ª´ng √¢m b√°o
    try {
      const answerDesc = await pcRef.current.createAnswer();
      await pcRef.current.setLocalDescription(answerDesc);
      console.log("üì§ Sending answer:", answerDesc);
      socketIo?.emit("call:signal", {
        peerId: callerId, // g·ª≠i v·ªÅ m√°y 1
        roomId,
        answer: answerDesc,
      });
    } catch (error) {
      console.error("‚ùå Error creating answer:", error);
    }
  };

  const listenCall = () => {
    const handleSignal = async ({ metadata }: any) => {
      if (!socketIo) {
        console.error("‚ùå No socket.io instance available");
        return;
      }
      const { peerId, roomId, offer, answer, candidate, type } = metadata;
      if (!peerId || !roomId) {
        console.error("‚ùå Invalid signal: missing peerId or roomId");
        return;
      }

      try {
        if (type && type === 'offer') {
          // N·∫øu ng∆∞·ªùi g·ªçi ƒë·∫øn thi·∫øt l·∫≠p k·∫øt n·ªëi m·ªõi cho ng∆∞·ªùi nh·∫≠n
          pendingOffer.current = new RTCSessionDescription(offer);
          return;
        }

        if (type && type === 'answer') {
          InCallManager.stopRingback();  // d·ª´ng √¢m b√°o
          // peerId l√† id ng∆∞·ªùi g·ªçi, ƒë·ªÉ l·∫•y ƒë√∫ng peer connection
          const pc = peerIdRef.current.get(peerId);
          if (!pc) {
            console.error("‚ùå No peer connection for answer from", peerId);
            return;
          }
          // Thi·∫øt l·∫≠p m√¥ t·∫£ t·ª´ xa
          await pc.setRemoteDescription(new RTCSessionDescription(answer));
          // C√°c ICE candidate ch·ªù
          if (pendingCandidates.current.has(peerId)) {
            const candidates = pendingCandidates.current.get(peerId);
            if (candidates && candidates.length > 0) {
              for (const c of candidates) {
                await pc.addIceCandidate(new RTCIceCandidate(c));
                console.log("‚úÖ Pending candidate added:", c);
              }
            }
            pendingCandidates.current.delete(peerId);
          }
          return;
        }
        // X·ª≠ l√Ω ICE candidate m·ªõi
        if (type && type === 'candidate' && candidate) {
          const pc = peerIdRef.current.get(peerId); // peerId l√† ng∆∞·ªùi G·ª¨I
          const candInit = candidate; // object JSON

          if (!pc) {
            // Ch∆∞a c√≥ PC -> queue theo fromPeer
            if (!pendingCandidates.current.has(peerId)) pendingCandidates.current.set(peerId, []);
            pendingCandidates.current.get(peerId)!.push(candInit);
            console.log("üì• Candidate queued (no PC yet) from:", peerId);
            return;
          }

          if (!pc.remoteDescription) {
            if (!pendingCandidates.current.has(peerId)) pendingCandidates.current.set(peerId, []);
            pendingCandidates.current.get(peerId)!.push(candInit);
            console.log("üì• Candidate queued (no remoteDescription) from:", peerId);
            return;
          }

          // ‚úÖ C√≥ PC v√† ƒë√£ setRemoteDescription -> add lu√¥n
          try {
            await pc.addIceCandidate(new RTCIceCandidate(candInit));
            console.log("‚úÖ addIceCandidate immediately:", candInit);
          } catch (e) {
            console.warn("‚ö†Ô∏è addIceCandidate failed, re-queue:", e);
            if (!pendingCandidates.current.has(peerId)) pendingCandidates.current.set(peerId, []);
            pendingCandidates.current.get(peerId)!.push(candInit);
          }
        }
      } catch (err) {
        console.error("‚ùå Error processing signal:", err);
      }
    };

    socketIo?.off("client:signal");
    socketIo?.on("client:signal", handleSignal);
  };

  const hangOut = () => {
    cleanUp();
    setConnectState('disconnected');
  };

  const toggleVideo = async (roomId: string) => {
    setIsVideoEnabled((prev) => !prev);
    if (localStreamRef.current) {
      const videoTrack = localStreamRef.current.getVideoTracks()[0];
      if (videoTrack) {
        videoTrack.enabled = !videoTrack.enabled;
        console.log(`‚úÖ Video track ${videoTrack.enabled ? 'enabled' : 'disabled'}`);
      }
    }
  };

  const toggleAudio = (roomId: string) => {
    setIsAudioEnabled((prev) => !prev);
    if (localStreamRef.current) {
      const audioTrack = localStreamRef.current.getAudioTracks()[0];
      if (audioTrack) {
        audioTrack.enabled = !audioTrack.enabled;
        console.log(`‚úÖ Audio track ${audioTrack.enabled ? 'enabled' : 'disabled'}`);
      }
    }
  };

  return {
    localStream,
    remoteStream,
    initStream,
    listenCall,
    setIsScreenSharing,
    handleCreateOffer,
    connectState,
    hangOut,
    toggleVideo,
    toggleAudio,
    isVideoEnabled,
    isAudioEnabled,
    handleAcceptCall
  };
};
